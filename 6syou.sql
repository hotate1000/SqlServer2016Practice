-- 並び替え
SELECT * FROM 社員 ORDER BY 給与 ASC;
GO
SELECT * FROM 社員 ORDER BY 給与 DESC;
GO
SELECT * FROM 社員 ORDER BY 給与 DESC, 社員番号 ASC;
GO

-- 条件AND
SELECT * FROM 社員 WHERE 給与 >= 500000 AND 給与 <= 700000;
SELECT * FROM 社員 BETWEEN 500000 AND 700000;
GO
-- 条件OR
SELECT * FROM 社員 WHERE 給与 = 300000 OR 給与 = 700000;
SELECT * FROM 社員 WHERE 給与 IN (300000, 700000);
GO
-- NULLの許可(NULLの書き換え)
SELECT * FROM 社員 WHERE 給与 IN (300000, 700000) OR 給与 IS NULL;
SELECT ISNULL(給与,1) FROM 社員;
GO

-- 文言検索LIKE
-- LIKE演算子とIN演算子は組み合わせ出来ない
SELECT * FROM 社員 WHERE 氏名 LIKE '田%';
SELECT * FROM 社員 WHERE 氏名 LIKE '_田%';
GO